
public class IteratorDemo
{

	public static void main(String[] args)
	{
		// while ,do while, for ,for-each
		
		//while
		//when you do not know the ending point and it depends on some external factor
		
		int i =1;
		while(i <= 5)
		{
			System.out.println("Hello");
			i++;
			
		}
		
		//do while ,it will print at least once even if user enters wrong value
		
		
		int j =8;
		do
		{
			System.out.println("world");
			j++;
			
		}while(j <= 5);
		
		
		//for loop all the three initialization, condition and increment in on single line
		//when you know starting and the ending point
		
		for(int k=1; k <= 5 ; k++)
		{
			System.out.println("Vinay");
			
		}
		
		
		

	}

}
