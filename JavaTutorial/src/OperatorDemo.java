
public class OperatorDemo 
{    
	/*
	 * Arithmetic +,-,*,/,%
	 * 
	 * 
	 */

	public static void main(String[] args)
	{
		int m=6,n=4;
		//n += m;//n= n + m
		n++;//n = n +1
		//n--;// n = n -1
		System.out.println(n);
		m= ++n;// pre increment ,value of n incremented and then assigned
		System.out.println(m);

	}

}
